package CalculadoraPOO;

public abstract class Subtrair {

    public double subtrair(double a, double b){
        return a-b;
    }

    public double subtrair(double a, double b, double c){
        return a-b-c;
    }

    public double subtrair(double a, double b, double c, double d){
        return a-b-c-d;
    }
}
