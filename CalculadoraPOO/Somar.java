package CalculadoraPOO;

public abstract class Somar {
    
    public double soma(double a, double b){
        return a+b;
    }

    public double soma(double a, double b, double c){
        return a+b+c;
    }

    public double soma(double a, double b, double c, double d){
        return a+b+c+d;
    }
}
